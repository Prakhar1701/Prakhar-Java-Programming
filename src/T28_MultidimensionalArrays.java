package com.company;

public class T28_MultidimensionalArrays {
    public static void main(String[] args) {
        //--------------------------------------------------------------------------------------------------------------
        //--------------------------------------------------------------------------------------------------------------
        // 2D array :
        //--------------------------------------------------------------------------------------------------------------
        int[][] flats_1;
        flats_1 = new int[2][3];                         //--> [rows] [column]  ;  rows=2, column=3

        flats_1[0][0] = 101;
        flats_1[0][1] = 102;
        flats_1[0][2] = 103;
        flats_1[1][0] = 201;
        flats_1[1][1] = 202;
        flats_1[1][2] = 203;
        //--------------------------------------------------------------------------------------------------------------
        int[][] flats_2 = {{11, 12, 13}, {21, 22, 23}};  //--> [rows] [column]  ; rows=2, column=3

        //--------------------------------------------------------------------------------------------------------------
        int[][] flats_3 = {{1, 2}, {3, 4}, {5, 6}};      //--> [rows] [column]  ; rows=3, column=2
        //--------------------------------------------------------------------------------------------------------------
        //Displaying Elements of 2D array using "for" loop:
        //--------------------------------------------------------------------------------------------------------------
        System.out.println("flats_1 :");

        for (int i = 0; i < flats_1.length; i++) {
            //flats_1.length = Number of rows =2
            for (int j = 0; j < flats_1[i].length; j++) {
                //flats_1[i].length = Number of column =3

                System.out.printf("%d  ", flats_1[i][j]);
            }
            System.out.println();
        }

        //--------------------------------------------------------------------------------------------------------------
        System.out.println();
        //--------------------------------------------------------------------------------------------------------------
        System.out.println("flats_2 :");

        for (int i = 0; i < flats_2.length; i++) {
            //flats_1.length = Number of rows =2
            for (int j = 0; j < flats_2[i].length; j++) {
                //flats_1[i].length = Number of column =3
                System.out.printf("%d  ", flats_2[i][j]);
            }
            System.out.println();
        }

        //--------------------------------------------------------------------------------------------------------------
        System.out.println();
        //--------------------------------------------------------------------------------------------------------------
        System.out.println("flats_3 :");

        for (int i = 0; i < flats_3.length; i++) {
            //flats_1.length = Number of rows =3
            for (int j = 0; j < flats_3[i].length; j++) {
                //flats_1[i].length = Number of column =2
                System.out.printf("%d  ", flats_3[i][j]);
            }
            System.out.println();
        }
        //--------------------------------------------------------------------------------------------------------------
        //--------------------------------------------------------------------------------------------------------------
        //Following the same process multidimensional array can be created.
    }
}
