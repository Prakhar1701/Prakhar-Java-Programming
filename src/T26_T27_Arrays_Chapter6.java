package com.company;

public class T26_T27_Arrays_Chapter6 {
    public static void main(String[] args) {

        //--------------------------------------------------------------------------------------------------------------
        //--------------------------------------------------------------------------------------------------------------
        //There are three main ways to create an array:
        //--------------------------------------------------------------------------------------------------------------
        //1. Declaration and memory allocation:

        int[] marks_1 = new int[3];

        //Initialization:

        marks_1[0] = 10;
        marks_1[1] = 15;
        marks_1[2] = 90;

        //--------------------------------------------------------------------------------------------------------------
        //2. Declaration and then memory allocation:

        int[] marks_2;
        marks_2 = new int[3];

        //Initialization:

        marks_2[0] = 20;
        marks_2[1] = 25;
        marks_2[2] = 65;

        //--------------------------------------------------------------------------------------------------------------
        //3. Declaration, memory allocation and initialization together:

        int[] marks_3 = {5, 17, 50};

        //--------------------------------------------------------------------------------------------------------------
        //Array indices starts from 0 and goes till (n-1) where n is the size of the array.
        //--------------------------------------------------------------------------------------------------------------
        //Printing length of arrays:
        System.out.println("Length of array \"marks_1\" : "+marks_1.length);
        System.out.println("Length of array \"marks_2\" : "+marks_2.length);
        System.out.println("Length of array \"marks_3\" : "+marks_3.length);

        //--------------------------------------------------------------------------------------------------------------
        System.out.println();
        //--------------------------------------------------------------------------------------------------------------
        //Printing arrays elements:     (Array Traversal using "for" loop)

        for (int i = 0; i < 3; i++) {
            System.out.printf("marks_1[%d]= %d ; marks_2[%d]= %d ; marks_3[%d]= %d\n", i, marks_1[i], i, marks_2[i], i, marks_3[i]);
        }

        //--------------------------------------------------------------------------------------------------------------
        System.out.println();
        //--------------------------------------------------------------------------------------------------------------
        //Overwriting a value and printing:

        marks_1[0] = 17;
        System.out.println("marks_1[0] : " + marks_1[0]);

        //--------------------------------------------------------------------------------------------------------------
//        marks_1[4]=80;                                    --> Throws an error
//        System.out.println("marks_1[4] : "+marks_1[4]);   --> Throws an error

        //--------------------------------------------------------------------------------------------------------------
        //--------------------------------------------------------------------------------------------------------------
    }
}
